package com.foodkonnekt.clover.vo;

public class ModifierViewVo {

    private Integer id;
    private String name;
    private Double price;
    private Integer productUsed;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getProductUsed() {
        return productUsed;
    }

    public void setProductUsed(Integer productUsed) {
        this.productUsed = productUsed;
    }

}
